package com.telusko.demo;

public class Alien {

	private int aid;
	private int aname;
    private String lang;
    
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public int getAname() {
		return aname;
	}
	public void setAname(int aname) {
		this.aname = aname;
	}
	public String getLang() {
		return lang;
	}
	public void setLang(String lang) {
		this.lang = lang;
	} 
    
    
}
